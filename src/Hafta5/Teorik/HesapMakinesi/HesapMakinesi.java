package Hafta5.Teorik.HesapMakinesi;

public class HesapMakinesi {

    public int topla(int t1,int t2){
        return(t1+t2);
    }
    public int topla(int t1,int t2,int t3){
        return(t1+t2+t3);
    }
    public float topla(float t1,float t2){
        return(t1+t2);
    }
    public float topla(float t1,float t2,float t3,float t4){
        return(t1+t2+t3+t4);
    }
}
